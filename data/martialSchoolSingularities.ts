// Singularidades das Escolas Marciais (Martial School Singularities)
// Cada escola marcial tem singularidades numeradas de I a XII

export interface MartialSchoolSingularity {
  id: string
  schoolId: string
  level: number // I, II, III, etc. (1-12)
  name: string
  description: string
  cost: number // Pontos de Evolução
  requirements: {
    previous?: string // ID da singularidade anterior necessária
    nivelAlma?: number // Nível de Alma mínimo
    aptitudes?: Record<string, number> // Aptidões necessárias
    attributes?: Record<string, number> // Atributos necessários
    skills?: Record<string, number> // Habilidades necessárias
  }
  effects: string // Descrição dos efeitos
  choices?: {
    id: string
    name: string
    description: string
  }[] // Escolhas opcionais (para singularidades que permitem escolha)
}

export interface MartialSchoolData {
  id: string
  name: string
  class: string // Classe (Disruptor, Agressor, Suporte, etc.)
  aptitude: string // Aptidão necessária
  tool: string // Ferramenta necessária
  toolNote?: string // Nota sobre a ferramenta
  suggestedAttributes: string[]
  suggestedSkills: string[]
  suggestedEquipment: string[]
  description: string
  singularities: MartialSchoolSingularity[]
}

